source = input("源文字: ")
print(LANGUAGES)
source_lang = input("源语言(字母代号): ")
target_lang = input("目标语言(字母代号): ")
translation_count = int(input("生草次数: "))


def get_random_lang():
    global source_lang, target_lang
    all_lang = []
    for lang in LANGUAGES:
        if lang != source_lang and lang != target_lang:
            all_lang.append(lang)
    return random.choice(all_lang)


current_lang = source_lang
current_text = source
translator = Translator(service_urls=['translate.google.cn'])

for i in range(0, translation_count):
    t = get_random_lang()
    print("[第" + str(i + 1) + "次生草]", "从", LANGUAGES[current_lang], "翻译到", LANGUAGES[t])
    trans = translator.translate(current_text, src=current_lang, dest=t)
    current_text = trans.text
    current_lang = t
    print("结果:", current_text)
    print("目标:", translator.translate(current_text, src=current_lang, dest=target_lang).text)

